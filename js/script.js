function CambiarColor(color){
		if(color=='1'){
			document.body.style.background='#f00';
		}
		if(color=='2'){
			document.body.style.background='#fff';
		}
		if(color=='3'){
			document.body.style.background='#000';
		}
		if(color=='4'){
			document.body.style.background='#2BD56E';
		}
	}
